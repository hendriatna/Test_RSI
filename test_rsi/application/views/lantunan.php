		var myAudio = document.getElementById("player"+count);
		var idEdit = document.getElementById(id);				
		var number = count % 10;
		var num = 0;
		if(number == 0){
			num = 1 + (count - 10);
		}else{
			num = 1 + (count - number);
		}
		$("audio[class=audio-mp3]").each(function(){
			if(!this.paused){
				var idAll = document.getElementById("pse"+num);
				this.pause();
				this.currentTime = 0;
				$('#pse'+num).find('span').toggleClass('glyphicon-pause').toggleClass('glyphicon-play-circle');
				idAll.id = 'ply'+num;
			}
			num++;
		});

		if(id == ('ply'+count)){
			myAudio.play();
			$('#'+id).find('span').toggleClass('glyphicon-play-circle').toggleClass('glyphicon-pause');
			idEdit.id = 'pse'+count;
		}else{
			myAudio.pause();
			$('#'+id).find('span').toggleClass('glyphicon-pause').toggleClass('glyphicon-play-circle');
			idEdit.id = 'ply'+count;
		}
	}

